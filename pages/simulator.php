                        <?php
    if (
        isset($_POST['InsideTemperature']) && isset($_POST['OutsideTemperature']) && isset($_POST['KRoof']) && isset($_POST['XRoof'])
        && isset($_POST['KInsulation']) && isset($_POST['XInsulation']) && isset($_POST['HouseLength']) && isset($_POST['HouseBreadth']) && isset($_POST['Time']) && isset($_POST['RateOfElectricity']) && isset($_POST['CostOfInsulator'])
    ) {

        $InsideTemperature = floatval($_POST['InsideTemperature']);
        $OutsideTemperature = floatval($_POST['OutsideTemperature']);
        $KRoof = floatval($_POST['KRoof']);
        $XRoof = floatval($_POST['XRoof']);
        $KInsulation = floatval($_POST['KInsulation']);
        $XInsulation = floatval($_POST['XInsulation']);
        $Area = floatval($_POST['HouseLength']) * floatval($_POST['HouseBreadth']);
        $Time = floatval($_POST['Time']);
        $RateOfElectricity = floatval($_POST['RateOfElectricity']);
        $CostOfInsulator = floatval($_POST['CostOfInsulator']);

        $dT = $OutsideTemperature - $InsideTemperature;
        $KA1 = $XRoof / $KRoof;
        $KA2 = $XInsulation / $KInsulation;

        $Q1 = - ($dT * $Area / $KA1) / 1000;
        $Q2 = - ($dT * $Area / ($KA1 + $KA2)) / 1000;

        $ecostpernight = $Q1 * $Time * $RateOfElectricity;
        $ecostperwinter = $ecostpernight * 120;

        $icostpernight = $Q2 * $Time * $RateOfElectricity;
        $icostperwinter = $icostpernight * 120;

        echo "<b>Without Insulation</b>";
        echo "<br><br>";
        echo "Heat Loss (Q1) = " . $Q1, " kW";
        echo "<br><br>";
        echo "Heat Loss for " . $Time, " hours = " . $Q1 * $Time, " kWh";
        echo "<br><br>";
        echo "Cost per night = $ " . $ecostpernight;
        echo "<br><br>";
        echo "Cost per winter = $ " . $ecostperwinter;
        echo "<br><br>";

        echo "<b>With Insulation</b>";
        echo "<br><br>";
        echo "Heat Loss (Q2) = " . $Q2, " kW";
        echo "<br><br>";
        echo "Heat Loss for " . $Time, " hours = " . $Q2 * $Time, " kWh";
        echo "<br><br>";
        echo "Cost per night = $ " . $icostpernight;
        echo "<br><br>";
        echo "Cost per winter = $ " . $icostperwinter;
        echo "<br><br>";

        echo "<b>Payback Period</b>";
        echo "<br><br>";
        echo "Total insulator cost = $ " . $XInsulation * $Area * $CostOfInsulator;
        echo "<br><br>";
        echo "Money saved per winter = $ " . ($ecostperwinter) - ($icostperwinter);
        echo "<br><br>";
        echo "Payback Period = " . $CostOfInsulator * $Area * $XInsulation / (($ecostperwinter) - ($icostperwinter));
        echo "<br><br>";

        $ecost = array();
        $heatloss = array();
        $thickness = array();
        $icost = array();
        $totalcost = array();
        $i = 0.01;
        for ($x = 0; $i <= $XRoof/2; $x+=1) {
            $mKA2 = ($i / $KInsulation);
            $mQ2 = - ($dT * $Area / ($KA1 + $mKA2)) / 1000;
            array_push($ecost, $mQ2 * $Time * $RateOfElectricity * 120);
            array_push($heatloss,$mQ2);
            array_push($thickness,$i);
            array_push($icost, $i * $Area * $CostOfInsulator);
            $i = $i + 0.001;
        }
        $ecost = array_map(function($v){return round($v,5);}, $ecost) ;
        $icost = array_map(function($v){return round($v,5);}, $icost) ;
        $heatloss = array_map(function($v){return round($v,5);}, $heatloss) ;
        $thickness = array_map(function($v){return round($v,3);}, $thickness) ;

        $totalcost = array_map(function () {
            return array_sum(func_get_args());
            }, $ecost, $icost);
        
            asort($totalcost, SORT_NUMERIC);
            $first_key = array_key_first($totalcost);

        echo "<b>Cost Optimization</b>";
        echo "<br><br>";
        echo "Thickness of Insulator = " . $thickness[$first_key];
        echo "<br><br>";
        echo "Cost of Electricity = $ " . $ecost[$first_key];
        echo "<br><br>";
        echo "Money Saved = $ " . ($ecostperwinter) - $ecost[$first_key];
        echo "<br><br>";
        echo "Cost of Insulation = $ " . $icost[$first_key];
        echo "<br><br>";
        echo "Payback Period = " . $icost[$first_key] / (($ecostperwinter) - $ecost[$first_key]);
        echo "<br><br>";
    
        
        
